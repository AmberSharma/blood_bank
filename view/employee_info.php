											<h1 class="entry-title">Basic Requirements for Blood Donation</h1>
					
					<div class="entry-content">
						<h3>Basic Requirements</h3>
<ul type="disc">
<li>Weight: At least 45 kg (100 Ibs) for both males and females</li>
<li>Well Being:&nbsp;Feeling well that day. Not having colds, coughs or 
flu in the last one week. No fever (Temperature &gt;37.5°C) in the last 3
 weeks.</li>
</ul>
<h3>Major Illness/Surgery</h3>
<p>Persons with the following conditions are&nbsp;<strong>not eligible</strong> for blood donation:</p>
<ul type="disc">
<li>Diseases of the heart or lungs (Donors who are asthmatic and without symptoms of asthma is eligible)</li>
<li>High blood pressure on medication</li>
<li>Diabetes on medication</li>
<li>Major surgery (can donate after 12 months)</li>
<li>AIDS or symptoms of AIDS, such as unexplained fevers, severe night 
sweats, unexpected weight loss, swollen glands, chronic diarrhoea or 
rare cancer</li>
<li>Epilepsy</li>
<li>Hepatitis B or C</li>
<li>Syphilis</li>
</ul>
<h3>Some Common Reasons For Temporary Deferral</h3>
<ul type="disc">
<li>Cold, flu or sore throat, please wait&nbsp;<strong>one week</strong> after recovery or treatment</li>
<li>Tooth extraction or dental work, please wait&nbsp;<strong>3 days</strong> after treatment</li>
<li>Skin infections (minor), wait&nbsp;<strong>1 week</strong> after complete healing</li>
<li>Normal pregnancy, please wait&nbsp;<strong>6 weeks</strong> after delivery and when you are not breast-feeding.</li>
<li>Travelled to a malaria endemic area such as rural areas in Malaysia, Thailand, Indonesia, etc, please wait&nbsp;<strong>6 weeks to 3 years</strong> and ask the attending doctor/nurse whether you are eligible for donation</li>
<li>Travelled to Philippines or India, please wait <strong>4 months</strong> before donation</li>
<li>Foreigners need to <strong>stay in Singapore for 2 months</strong> after donation</li>
<li>Close contact with Hepatitis B, wait&nbsp;<strong>12 months</strong> and after full course of hepatitis B vaccination (and shown a satisfactory antibody response)</li>
<li>Infectious Diseases e.g. Chickenpox, Measles, Dengue, wait&nbsp;<strong>6 months</strong> after recovery</li>
</ul>
<h3>Medication</h3>
<p>People who are taking drugs for cancer treatment, heart diseases, 
high blood pressure, diabetes or current infections will not be accepted
 as blood donors.</p>
<p>For those taking:</p>
<table>
<tbody>
<tr>
<td>Traditional Medication</td>
<td>Please wait 3 days</td>
</tr>
<tr>
<td>Antibiotics</td>
<td>Please wait 1 week</td>
</tr>
<tr>
<td>Anti-malaria Medication</td>
<td>Please wait 4 weeks</td>
</tr>
</tbody>
</table>
<p>For our criteria for vaccination, please <a href="http://www.hsa.gov.sg/html/consumer/00000000000000001423.html">visit our website</a></p>
<h3>Travel to Europe</h3>
<p>With effect from 1 July 2002, you will be ineligible to donate blood&nbsp;<strong>indefinitely</strong> if you fall into EITHER one of the following 2 groups:</p>
<ol type="1">
<li>You have visited or lived in the&nbsp;<strong>United Kingdom</strong> (England, Northern Ireland, Scotland, Wales, the Isle of Man, or the Channel Island) from&nbsp;<strong>1980 through 1996</strong> for a&nbsp;<strong>cumulative period</strong> <strong>of 3 months or more</strong>, or you have&nbsp;<strong>received a transfusion of blood or blood components in the United Kingdom</strong> (England, Northern Ireland, Scotland, the Isle of Man, or the Channel Island) between 1980 till now.</li>
<li>You have visited or lived in&nbsp;<strong>France</strong> from&nbsp;<strong>1980 till now</strong> for a&nbsp;<strong>cumulative period of 5 years or more</strong>.</li>
</ol>
<p>However, if you have stayed in Europe (besides United Kingdom and 
France) for a cumulative period of more than 5 years, you can 
donate&nbsp;<strong>plasma</strong> only through apheresis method.</p>
<h3>Tattoo, Ear Piercing or Acupuncture</h3>
<p>This would require a deferment period of 12 months, unless the needles used are disposable and the whole procedure is sterile.</p>
<p>The above criteria cover some common situations that will render a 
prospective donor unsuitable for blood donation. The doctor or nurse 
will exercise medical judgment during the medical interview for each 
individual donor.</p>
<p>Donors who are interested in donating blood will have to bring along 
their NRIC or donation card for registration. Non-Singaporeans will have
 to produce their passports.</p>
<div style="background-color: #eee; padding: 4px; font-size: 0.9em;">
<h3>Note regarding Endogenous Human Clinical vCJD</h3>
<p>CTM-HSA has adopted the policy of excluding donors from the United 
Kingdom (UK) and most European countries due to the incidence of 
endogenous human clinical vCJD (“Mad Cow Disease”) infections and 
outbreaks of vCJD in their cattle industry. Donors who have resided in 
UK for a cumulative period of 3 months or more from 1980 to 1996 are 
currently restricted from blood donation because of a small risk of 
possible vCJD infection. Donors who have resided for 5 years or more in 
France are also under similar restrictions.</p>
<p>Donors who have stayed in other European countries (excluding France 
and UK) for 5 years or more can still donate blood but are advised to 
donate only plasma via apheresis technology.</p>
<p>The guidelines of the Bloodbank@HSA are in harmony with international
 practice. This restriction is an international precautionary measure 
enforced by all blood banks of developed countries. They are solely a 
purely precautionary measure to ensure the safety of the local blood 
supply.</p>
<p>I am pleased to inform you that we have produced a poster to inform 
donors about some common criteria that make them not suitable to donate 
blood.</p>
<p>Unfortunately only the common, basic information on donation can be 
disseminated through these means. There is a limitation on the type and 
amount of information on donation criteria that we can disseminate in 
this way. There are numerous situations and scenarios that would make 
one unsuitable to donate blood. Therefore we have provided detailed 
information on our blood donation criteria at our Donor-care web site at
 <a href="http://www.hsa.gov.sg/html/consumer/donorcare.html">http://www.hsa.gov.sg/html/consumer/donorcare.html</a>.
 A copy of the pre-donation health questionnaire (with full explanatory 
notes) and the various blood donation criteria is available on this web 
site for easy reference at your convenience.</p>
<p>In addition, you can also call Bloodbank@HSA directly at 6213 0626 or
 contact the Donor Managers of the Singapore Red Cross Society at 6220 
0183 if you have any queries on blood donation.</p>
<p>Shirley Ng</p>
<p>Blood Donor Recruitment Programme</p>
<p>Singapore Red Cross Society</p>
</div>
